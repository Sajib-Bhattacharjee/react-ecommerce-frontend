// Helper functions to fetch mock data
export const getProducts = (limit?: number, filter?: Partial<Product>) => {
  let result = [...products];

  if (filter) {
    const filterEntries = Object.entries(filter);
    result = result.filter((product) =>
      filterEntries.every(
        ([key, value]) => product[key as keyof Product] === value
      )
    );
  }

  if (limit && limit > 0) {
    result = result.slice(0, limit);
  }

  return result;
};

export const getProductById = (id: number) => {
  return products.find((product) => product.id === id);
};

export const getProductBySlug = (slug: string) => {
  return products.find((product) => product.slug === slug);
};

export const getCategories = (parent_id?: number) => {
  if (parent_id !== undefined) {
    return categories.filter((category) => category.parent_id === parent_id);
  }
  return categories;
};

export const getCategoryById = (id: number) => {
  return categories.find((category) => category.id === id);
};

export const getBrands = () => {
  return brands;
};

export const getBrandById = (id: number) => {
  return brands.find((brand) => brand.id === id);
};

export const getReviewsByProductId = (productId: number) => {
  return reviews.filter((review) => review.product_id === productId);
};

export const getFeaturedProducts = (limit?: number) => {
  return getProducts(limit, { featured: true });
};

export const getNewArrivals = (limit?: number) => {
  return getProducts(limit, { new_arrival: true });
};

export const getBestSellers = (limit?: number) => {
  return getProducts(limit, { bestseller: true });
};

export const getTrendingProducts = (limit?: number) => {
  return getProducts(limit, { trending: true });
};

export const getFeaturedCategories = () => {
  return categories.filter((category) => category.featured);
};

export const searchProducts = (query: string) => {
  const lowerCaseQuery = query.toLowerCase();
  return products.filter(
    (product) =>
      product.name.toLowerCase().includes(lowerCaseQuery) ||
      product.description.toLowerCase().includes(lowerCaseQuery)
  );
};

// Helper functions to convert from Product to other interfaces
export const productToCartItem = (
  product: Product,
  quantity: number = 1,
  options?: { color?: string; size?: string }
): CartItem => {
  return {
    id: product.id,
    name: product.name,
    price: product.price,
    quantity: quantity,
    image: product.images[0],
    discount: product.discount_price
      ? Math.round(
          ((product.price - product.discount_price) / product.price) * 100
        )
      : undefined,
    ...options,
  };
};

export const productToWishlistItem = (product: Product): any => {
  return {
    id: product.id,
    name: product.name,
    price: product.price,
    images: product.images,
    discount: product.discount_price
      ? Math.round(
          ((product.price - product.discount_price) / product.price) * 100
        )
      : undefined,
    inStock: product.inStock,
  };
};

// Helper function to convert API/database Product format to component format
export const productToComponentFormat = (product: Product): any => {
  return {
    id: product.id,
    name: product.name,
    price: product.price,
    discountPrice: product.discount_price,
    images: product.images,
    rating: product.ratings,
    ratingCount: product.reviews_count,
    bestseller: product.bestseller,
    trending: product.trending,
    newArrival: product.new_arrival,
    featured: product.featured,
    inStock: product.inStock,
    description: product.description,
    slug: product.slug,
  };
};

export const getDeals = (): Product[] => {
  return products.filter(
    (product) =>
      product.discount_price && product.discount_price < product.price
  );
};
